package session3;

public class MultiArrays {
    public static void main(String[] args) {
        int grid[][] = new int[12][8];
        grid[0][0] = 8;
        grid[1][1] = 5;

        System.out.println("grid.length");
        System.out.println("grid[0].length");
        System.out.println("grid[11].length");
    }
}
